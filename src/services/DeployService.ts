function generateGCPConfig(config: DeployConfig): GenerateResult {
  const appName = sanitizeAppName(config.appName);
  const port = config.port ?? 3000;
  const region = config.region ?? 'us-central1';

  const memoryGb = config.memory
    ? parseFloat(config.memory) / 1024
    : 0.5;

  const envVarLines = Object.entries(config.env ?? {})
    .map(([k, v]) => `  ${k}: "${sanitizeEnvValue(v)}"`)
    .join('\n');

  const appYaml = `# Generated by Lisa — Google Cloud App Engine
runtime: custom
env: flex

service: ${appName}

env_variables:
  NODE_ENV: production
  PORT: "${port}"
${envVarLines}

resources:
  cpu: ${config.cpus ?? 1}
  memory_gb: ${memoryGb}

automatic_scaling:
  min_num_instances: 0
  max_num_instances: 1
`;

  return {
    success: true,
    files: [{ path: 'app.yaml', content: appYaml }],
    instructions: `Deploy with: gcloud app deploy --region=${region}`,
  };
}

function generateVercelConfig(config: DeployConfig): GenerateResult {
  const appName = sanitizeAppName(config.appName);
  const region = config.region ?? 'iad1';

  const envEntries = Object.entries(config.env ?? {}).reduce(
    (acc, [k, v]) => {
      acc[k] = sanitizeEnvValue(v);
      return acc;
    },
    {} as Record<string, string>
  );

  const vercelJson = JSON.stringify(
    {
      name: appName,
      version: 2,
      regions: [region],
      framework: 'vite',
      buildCommand: 'npm run build',
      outputDirectory: 'dist',
      env: {
        NODE_ENV: 'production',
        ...envEntries,
      },
      headers: [
        {
          source: '/assets/(.*)',
          headers: [
            { key: 'Cache-Control', value: 'public, max-age=31536000, immutable' },
          ],
        },
      ],
    },
    null,
    2
  );

  return {
    success: true,
    files: [{ path: 'vercel.json', content: vercelJson }],
    instructions: `Deploy with: vercel --prod\nOr connect your repo at vercel.com for automatic deployments.`,
  };
}

function generateNetlifyConfig(config: DeployConfig): GenerateResult {
  const appName = sanitizeAppName(config.appName);

  const envLines = Object.entries(config.env ?? {})
    .map(([k, v]) => `  ${k} = "${sanitizeEnvValue(v)}"`)
    .join('\n');

  const netlifyToml = `# Generated by Lisa
[build]
  command = "npm run build"
  publish = "dist"

[build.environment]
  NODE_ENV = "production"
${envLines}

[[redirects]]
  from = "/*"
  to = "/index.html"
  status = 200

[[headers]]
  for = "/assets/*"
  [headers.values]
    Cache-Control = "public, max-age=31536000, immutable"

[context.production]
  environment = { NODE_ENV = "production" }
`;

  const siteJson = JSON.stringify(
    {
      name: appName,
    },
    null,
    2
  );

  return {
    success: true,
    files: [
      { path: 'netlify.toml', content: netlifyToml },
      { path: '.netlify/state.json', content: siteJson },
    ],
    instructions: `Deploy with: netlify deploy --prod\nOr connect your repo at app.netlify.com for automatic deployments.`,
  };
}

