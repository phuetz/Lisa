/**
 * Generate deployment configuration files for a given cloud platform.
 */
export function generateDeployConfig(config: DeployConfig): GenerateResult {
  const generator = PLATFORM_GENERATORS[config.platform];
  if (!generator) {
    return {
      success: false,
      files: [],
      instructions: `Unknown platform: ${config.platform}. Supported platforms: ${Object.keys(PLATFORM_GENERATORS).join(', ')}`,
    };
  }

  return generator(config);
}

/**
 * Get detailed CLI deployment instructions for a platform.
 */
export function getDeployInstructions(platform: CloudPlatform): string {
  return DEPLOY_INSTRUCTIONS[platform] ?? `No instructions available for platform: ${platform}`;
}

/**
 * Get the list of all supported cloud platforms.
 */
export function getSupportedPlatforms(): CloudPlatform[] {
  return Object.keys(PLATFORM_GENERATORS) as CloudPlatform[];
}

/**
 * Trigger a browser download of generated deployment config files.
 * Creates a single file download for single-file configs, or a combined
 * text file with all configs for multi-file configs.
 *
 * Uses Blob + URL.createObjectURL + anchor click pattern (browser-compatible).
 */
export function downloadConfig(result: GenerateResult): void {
  if (!result.success || result.files.length === 0) {
    return;
  }

  if (result.files.length === 1) {
    // Single file: download directly
    const file = result.files[0];
    downloadFile(file.path, file.content);
  } else {
    // Multiple files: combine into a single downloadable text
    const combined = result.files
      .map((f) => `# ===== ${f.path} =====\n${f.content}`)
      .join('\n\n');
    const primaryName = result.files[0].path.replace(/\.[^.]+$/, '') + '-bundle.txt';
    downloadFile(primaryName, combined);
  }
}

/**
 * Download individual config files one by one.
 * Useful when the user wants separate files instead of a bundle.
 */
export function downloadConfigFiles(result: GenerateResult): void {
  if (!result.success || result.files.length === 0) {
    return;
  }

  for (const file of result.files) {
    downloadFile(file.path, file.content);
  }
}

/**
 * Trigger a browser download of a single file.
 */
function downloadFile(filename: string, content: string): void {
  // Extract just the filename (strip any directory prefix like ".netlify/")
  const basename = filename.split('/').pop() ?? filename;

  const blob = new Blob([content], { type: 'text/plain;charset=utf-8' });
  const url = URL.createObjectURL(blob);

  const anchor = document.createElement('a');
  anchor.href = url;
  anchor.download = basename;
  anchor.style.display = 'none';

  document.body.appendChild(anchor);
  anchor.click();

  // Clean up
  document.body.removeChild(anchor);
  URL.revokeObjectURL(url);
}
